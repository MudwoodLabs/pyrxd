def skeleton(s: str) -> str:
    """Apply the TR39 confusable reduction to every character of *s*.

    Two strings that visually 'look the same' produce identical
    skeletons. Use ``skeleton(a) == skeleton(b)`` to detect
    homoglyph spoofing.

    The mapping is pre-transitively-closed at vendor time, so this
    runs in O(len(s)) with a single dict lookup per char. Pure ASCII
    inputs round-trip unchanged (no codepoint in 0x20-0x7E maps to
    anything in the table).
    """
    if not isinstance(s, str):
        return s
    return "".join(_CONFUSABLE_MAP.get(ord(ch), ch) for ch in s)
